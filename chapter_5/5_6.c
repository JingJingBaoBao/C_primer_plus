#include <stdio.h>
int main(void)
{
    printf("int division: 5 / 4 is %d\n", 5 / 4);
    printf("int division: 6 / 4 is %d\n", 6 / 4);
    printf("int division: 7 / 4 is %d\n", 7 / 4);
    printf("float division: 7. / 4. is %1.2f\n", 7. / 4.);
    printf("mixed division: 7. / 4 is %1.2f\n", 7 / 4.);

    return 0;
}